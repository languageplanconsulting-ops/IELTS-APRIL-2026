/**
 * Freezes the spelling gate into `src/writingTask2RealWords.ts`.
 *
 * Enumerates every string that could ever appear as a Writing Task 2 MCQ option
 * — authored options, blank answers, and every candidate `buildMorphDistractors`
 * can invent from those answers — keeps only the ones `isRealWord` accepts, and
 * writes them out as a frozen set. The app imports that set instead of shipping
 * a dictionary; `verify:task2-spelling` fails the build if a live option ever
 * falls outside it.
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { isRealWord, isSingleWord, loadDictionary } from './task2-real-word-gate.mjs'
import { buildMorphDistractors } from '../src/writingTask2Harden.ts'
import { WGB2_EXERCISES, assembleTask2Essay } from '../src/writingTask2Builder.ts'
import { WGB2_DENSE_OVERRIDES } from '../src/writingTask2Dense/index.ts'
import { GENERAL_TRAINING_TASK2_BUILDERS } from '../src/writingGeneralTask2Builders.ts'
import { WGB2_DENSITY_SELECT_OPTIONS, wordFormCandidates } from '../src/writingTask2QuestionDensity.ts'
import { WGB2_OPTION_FILLERS } from '../src/writingTask2Fillers.ts'
import { WRITING_TASK2_PROMPTS } from '../src/writingTask2Data.ts'

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const OUT = path.join(ROOT, 'src/writingTask2RealWords.ts')

const dict = loadDictionary()

// Corpus of human-written model-essay prose. An inflection is only accepted as
// an option if it actually occurs here — spelling rules alone cannot tell that
// "reasons" is English while "facted" is not.
const corpus = new Set()
for (const exercise of [...WGB2_EXERCISES, ...WGB2_DENSE_OVERRIDES, ...GENERAL_TRAINING_TASK2_BUILDERS]) {
  for (const paragraph of assembleTask2Essay(exercise)) {
    for (const word of paragraph.text.match(/[A-Za-z]+/g) || []) corpus.add(word.toLowerCase())
  }
}
for (const prompt of WRITING_TASK2_PROMPTS) {
  for (const paragraph of prompt.paragraphs) {
    for (const word of paragraph.text.match(/[A-Za-z]+/g) || []) corpus.add(word.toLowerCase())
  }
}

const candidates = new Set()
const consider = (value) => {
  if (!value || !isSingleWord(value)) return
  candidates.add(String(value).trim().toLowerCase())
}
/** A word plus every foil the two generators can derive from it. */
const seed = (word) => {
  consider(word)
  if (!word || !isSingleWord(word)) return
  for (const foil of buildMorphDistractors(word)) consider(foil)
  for (const form of wordFormCandidates(word)) consider(form)
}

for (const exercise of [...WGB2_EXERCISES, ...WGB2_DENSE_OVERRIDES, ...GENERAL_TRAINING_TASK2_BUILDERS]) {
  for (const step of exercise.steps) {
    for (const segment of step.segments) {
      if (segment.kind !== 'blank') continue
      const blank = segment.blank
      const words = [
        ...(blank.options || []),
        ...(blank.answers || []),
        ...(blank.answer ? [blank.answer] : [])
      ]
      // Foils are only ever built from an answer or an option, so seed from all.
      for (const word of words) seed(word)
    }
  }
}

for (const word of [...WGB2_DENSITY_SELECT_OPTIONS, ...WGB2_OPTION_FILLERS]) seed(word)

// Density rules and general-training builders mint options from prose that the
// structural walk above cannot see, so also sweep every word in the Task 2
// sources. Over-collecting is harmless: the gate rejects whatever is not real.
const SOURCE_FILES = [
  'src/writingTask2Builder.ts',
  'src/writingTask2Data.ts',
  'src/writingTask2QuestionDensity.ts',
  'src/writingGeneralTask2Builders.ts',
  'src/writingGeneralTask2Data.ts',
  'src/writingTask2Dense/t2-twe-3.ts',
  'src/writingTask2Dense/t2-twe-4.ts',
  'src/writingTask2Dense/t2-twe-5.ts',
  'src/writingTask2Dense/t2-twe-6.ts'
]
for (const file of SOURCE_FILES) {
  const full = path.join(ROOT, file)
  if (!fs.existsSync(full)) continue
  for (const word of fs.readFileSync(full, 'utf8').match(/[A-Za-z]{2,}/g) || []) seed(word)
}

// Two tiers. `real` protects authored options: it only rules out invented
// forms. `attested` is stricter and gates the runtime generators — a form it
// invents must actually occur in the model-essay prose, because spelling rules
// cannot tell that "reasons" is English while "facted" is not.
const accepted = [...candidates].filter((word) => isRealWord(word, dict)).sort()
const attested = accepted.filter((word) => dict.has(word) || corpus.has(word))
const rejected = candidates.size - accepted.length

const body = `// AUTO-GENERATED by scripts/build-task2-real-words.mjs — do not edit by hand.
// Every Writing Task 2 multiple-choice option must be a real English word:
// the exam tests grammar and word choice, not spelling. Run
// \`npm run build:task2-real-words\` after adding Task 2 content.

export const WGB2_REAL_WORDS: ReadonlySet<string> = new Set([
${accepted.map((word) => `  '${word}'`).join(',\n')}
])

/** Subset that actually occurs in the model essays — see isAttestedTask2Word. */
export const WGB2_ATTESTED_WORDS: ReadonlySet<string> = new Set([
${attested.map((word) => `  '${word}'`).join(',\n')}
])

/**
 * True when \`word\` is safe to offer as a multiple-choice option. Phrases,
 * hyphenated forms and the deliberate "leave it blank" option pass through —
 * this gate only rules out invented single words.
 */
export const isRealTask2Word = (word: string): boolean => {
  const value = String(word ?? '').trim()
  if (!value) return true
  if (!/^[A-Za-z]+$/.test(value)) return true
  return WGB2_REAL_WORDS.has(value.toLowerCase())
}

/**
 * Stricter gate for automatically generated foils: the form must be attested in
 * the model-essay prose, not merely spellable. Keeps generators from inventing
 * plausible-looking non-words such as "facted" or "practicaled".
 */
export const isAttestedTask2Word = (word: string): boolean => {
  const value = String(word ?? '').trim()
  if (!value) return false
  if (!/^[A-Za-z]+$/.test(value)) return false
  return WGB2_ATTESTED_WORDS.has(value.toLowerCase())
}
`

fs.writeFileSync(OUT, body)
console.log(
  `wrote ${accepted.length} real words (${attested.length} attested) to src/writingTask2RealWords.ts, rejected ${rejected} invented forms`
)
